import streamlit as st

def show():
    # Τίτλος σελίδας
    st.header("🔥 Multizone Θέρμανση & Ψύξη με MCO MH8")
    st.write(
        "Πλήρης, έξυπνη και ανεξάρτητη διαχείριση θέρμανσης & ψύξης ανά δωμάτιο, "
        "με χρήση θερμοστατών MCO MH8 και αντλίας θερμότητας ως κοινή πηγή."
    )

    st.divider()

    # 1. Βασική Ιδέα
    st.subheader("📋 Βασική Αρχή Συστήματος")
    st.markdown(
        """
        Σε σύγχρονες κατοικίες και επαγγελματικούς χώρους όπου απαιτείται **ανεξάρτητος έλεγχος θερμοκρασίας ανά δωμάτιο**, 
        το MCO MH8 μπορεί να λειτουργήσει ως **ενιαίος θερμοστάτης θέρμανσης & ψύξης** για κάθε ζώνη.

        Κάθε ζώνη διαθέτει:
        - δική της **ηλεκτροβάνα ενδοδαπέδιας** (θέρμανση)  
        - δική της **ηλεκτροβάνα fan coil** (ψύξη)  
        - δικό της **fan coil οροφής**  
        - δικό της **θερμοστάτη MCO MH8**  

        Η **αντλία θερμότητας** λειτουργεί ως κοινή πηγή ζεστού/κρύου νερού για όλες τις ζώνες, 
        ενώ ένας κεντρικός controller καθορίζει το συνολικό mode λειτουργίας (HEAT ή COOL).
        """
    )

    st.divider()

    # 2. Λειτουργία Θέρμανσης
    with st.expander("🌡️ Λειτουργία Θέρμανσης (HEAT Mode)", expanded=True):
        st.markdown(
            """
            Όταν το σύστημα βρίσκεται σε λειτουργία **θέρμανσης (HEAT)**:

            - Το MH8 ανοίγει **μόνο** τη βαλβίδα της ενδοδαπέδιας της ζώνης.  
            - Η βαλβίδα του fan coil παραμένει κλειστή.  
            - Ο ανεμιστήρας του fan coil είναι **απενεργοποιημένος**.  
            - Η θερμοκρασία χώρου ρυθμίζεται από το MH8 με βάση το setpoint.  
            - Όταν επιτευχθεί η επιθυμητή θερμοκρασία, η βαλβίδα ενδοδαπέδιας κλείνει.  

            Έτσι, κάθε δωμάτιο λειτουργεί ως **ανεξάρτητη θερμική ζώνη**, χωρίς να απαιτείται δεύτερος θερμοστάτης.
            """
        )

    # 3. Λειτουργία Ψύξης
    with st.expander("❄️ Λειτουργία Ψύξης (COOL Mode)", expanded=True):
        st.markdown(
            """
            Όταν το σύστημα βρίσκεται σε λειτουργία **ψύξης (COOL)**:

            - Το MH8 κλείνει **όλες** τις βαλβίδες ενδοδαπέδιας, ώστε να μην περνά κρύο νερό στο δάπεδο.  
            - Ανοίγει τη βαλβίδα του fan coil της ζώνης.  
            - Ενεργοποιεί τον ανεμιστήρα του fan coil (Low / Medium / High).  
            - Η θερμοκρασία χώρου ρυθμίζεται από το MH8.  
            - Όταν επιτευχθεί το setpoint, η βαλβίδα fan coil κλείνει και ο ανεμιστήρας σταματά.  

            Με αυτόν τον τρόπο, κάθε δωμάτιο διαθέτει **ανεξάρτητη ψύξη**, πλήρως ελεγχόμενη από τον ίδιο θερμοστάτη.
            """
        )

    # 4. AUTO Mode
    with st.expander("🔁 AUTO Mode – Αυτόματη Εναλλαγή"):
        st.markdown(
            """
            Στο **AUTO mode**:

            - Το MH8 αποφασίζει αν χρειάζεται θέρμανση ή ψύξη, με βάση τη θερμοκρασία χώρου.  
            - Ο κεντρικός controller επιλέγει το συνολικό mode της αντλίας θερμότητας (HEAT ή COOL).  
            - Αν υπάρχουν αντικρουόμενες απαιτήσεις (π.χ. μία ζώνη ζητά HEAT και άλλη COOL), 
              εφαρμόζεται **πολιτική προτεραιότητας** (συνήθως προτεραιότητα στην ψύξη).  
            """
        )

    st.divider()

    # 5. Κεντρική Λογική
    st.subheader("🧠 Κεντρική Λογική Συστήματος")
    st.markdown(
        """
        Ο κεντρικός controller:

        - Θέτει την αντλία θερμότητας σε **HEAT**, όταν τουλάχιστον μία ζώνη ζητά θέρμανση.  
        - Θέτει την αντλία θερμότητας σε **COOL**, όταν τουλάχιστον μία ζώνη ζητά ψύξη.  
        - Σβήνει την αντλία, όταν **καμία ζώνη** δεν ζητά λειτουργία.  

        Έτσι επιτυγχάνεται:
        - **μέγιστη εξοικονόμηση ενέργειας**  
        - **απόλυτος έλεγχος** της εγκατάστασης  
        - **μείωση φθοράς** της αντλίας θερμότητας  
        """
    )

    st.divider()

    # 6. Προστασίες
    with st.expander("🛡️ Προστασίες & Ασφάλεια"):
        st.markdown(
            """
            Το σύστημα εξασφαλίζει:

            - **Προστασία ενδοδαπέδιας**:  
              Δεν περνά ποτέ κρύο νερό στο δάπεδο, αποφεύγοντας υγρασίες και συμπυκνώματα.  

            - **Προστασία fan coil**:  
              Δεν λειτουργεί ποτέ χωρίς ανοιχτή βαλβίδα ή χωρίς νερό στο κύκλωμα.  

            - **Προστασία αντλίας θερμότητας**:  
              Αποφεύγονται συνεχείς αλλαγές mode και άσκοπα ON/OFF, αυξάνοντας τη διάρκεια ζωής της.  
            """
        )

    st.divider()

    # 7. Πλεονεκτήματα
    st.subheader("🟩 Πλεονεκτήματα Λύσης MH8 Multizone")
    st.markdown(
        """
        - **Ένας θερμοστάτης ανά δωμάτιο** – όχι δύο συσκευές στον τοίχο.  
        - Πλήρης διαχείριση **θέρμανσης & ψύξης** από μία ενιαία διεπαφή.  
        - **Ανεξάρτητες ζώνες** με μέγιστη άνεση για τον χρήστη.  
        - **Σημαντική εξοικονόμηση ενέργειας**, καθώς κάθε ζώνη λειτουργεί μόνο όταν χρειάζεται.  
        - **Z‑Wave επικοινωνία** με κεντρικό controller και δυνατότητα ενσωμάτωσης σε Smart Home σενάρια.  
        - **Premium εμφάνιση** και υψηλή αξιοπιστία, κατάλληλη για κατοικίες, Airbnb, ξενοδοχεία και επαγγελματικούς χώρους.  
        """
    )

    st.divider()

    # 8. Υδραυλική Αρχιτεκτονική
    with st.expander("🧱 Υδραυλική Αρχιτεκτονική – Περιγραφή"):
        st.markdown(
            """
            Η εγκατάσταση αποτελείται από:

            - **Αντλία θερμότητας** ως κοινή πηγή ζεστού/κρύου νερού.  
            - **Συλλέκτη ενδοδαπέδιας** με ηλεκτροβάνες ανά ζώνη (Δωμάτιο 1, Δωμάτιο 2, κ.λπ.).  
            - **Fan coil οροφής** σε κάθε ζώνη, με δική του ηλεκτροβάνα.  
            - **Θερμοστάτη MCO MH8** σε κάθε δωμάτιο, που ελέγχει:
              - τη βαλβίδα ενδοδαπέδιας (HEAT)  
              - τη βαλβίδα fan coil (COOL)  
              - τον ανεμιστήρα του fan coil  
            - **Κεντρικό controller**, που συντονίζει το συνολικό mode (HEAT/COOL) της αντλίας θερμότητας.  

            Το αποτέλεσμα είναι ένα **πλήρως ανεξάρτητο multizone σύστημα**, 
            με υψηλή άνεση, εξοικονόμηση ενέργειας και καθαρή, επαγγελματική αρχιτεκτονική.
            """
        )

    st.success("✅ Η λύση MH8 Multizone προσφέρει πραγματική ανεξαρτησία ανά δωμάτιο, με μία μόνο συσκευή ανά ζώνη.")
